
# Keyword argument

def person(name,address,age):
    
    print("Name :" ,name)
    print("Address :",address)
    print("Age : ",age)

# person("kathmandu","Ram",23) positional argument



person(name="sachin",address="kathmandu",age=51) #keyword argument
print("=================================================================")

# we can also do this
person("sachin",address="kathmandu",age=555)

print("=================================================================")